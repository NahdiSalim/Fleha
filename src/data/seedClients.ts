function buildInvoice(
  id: string,
  invoiceId: string,
  clientId: string,
  date: string,
  products: InvoiceProduct[],
  paid: number
): Invoice {
  const total = Math.round(products.reduce((s, p) => s + p.total, 0) * 100) / 100;
  const balanceDue = Math.round((total - paid) * 100) / 100;

  return {
    id,
    invoiceId,
    clientId,
    date,
    products,
    total,
    paid,
    balanceDue: Math.max(0, balanceDue),
    status: balanceDue <= 0 ? "paid" : "partially_paid",
  };
}

